/**
 * Internal dependencies
 */

import { defaultTheme } from '../theme.js';
import { themeToCss } from '../utils.js';

printStylesheet( defaultTheme );

function printStylesheet( theme ) {
	const css = themeToCss( theme );

	const contents = [
		`/* Generated by WordPress */`,
		'\n\n',
		':root {',
		'\n',
		css,
		'\n',
		'}',
	];

	return contents;
}
